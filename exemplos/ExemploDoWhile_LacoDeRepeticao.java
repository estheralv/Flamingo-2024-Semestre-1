package exemplos;

import java.util.Scanner;

public class ExemploDoWhile_LacoDeRepeticao {
	public static void main(String[] args) {
	
		Scanner sc = new Scanner(System.in);
		
		int contadora = 0;
		
		do {
			System.out.println("Jailson");
			contadora++;
		} while (contadora < 4);
		
			sc.close();
	
	}
}
