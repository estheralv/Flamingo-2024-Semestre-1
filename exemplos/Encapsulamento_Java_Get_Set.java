package exemplos;

public class Encapsulamento_Java_Get_Set {
	
	private String nome;
		
		public String getNome() {
			return nome;
		}
		
		public void setNome(String newNome) {
			this.nome = newNome;
		}
		
		private int idade;
	
		
		public int getIdade() {
			return idade;
		}
		
		public void setIdade(int newIdade) {
			this.idade = newIdade;
		}

}
